package by.home.maxzzzit;

public class PrivedenieChisla {
	public static void main(String[]args) {
		int a=54;
		double d;
		d=(double)a;
		System.out.println("Перевод числа из целого (int) в вещественное "+d);
		
		long lg=564392018;
		float f;
		f=(float)lg;
		System.out.println("Перевод числа из целого длинного (long) в вещественное "+f);
		
		double db = 3654.7032;
		int i;
		i=(int)db;
		System.out.println("Перевод числа из вещественного (double) в целое "+i);
	}
}
